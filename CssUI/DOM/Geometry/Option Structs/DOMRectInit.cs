namespace CssUI.DOM.Geometry
{
    public class DOMRectInit
    {
        #region Properties
        public readonly double X = 0;
        public readonly double Y = 0;
        public readonly double Width = 0;
        public readonly double Height = 0;
        #endregion

        public DOMRectInit(double x, double y, double width, double height)
        {
            X = x;
            Y = y;
            Width = width;
            Height = height;
        }
    }
}
